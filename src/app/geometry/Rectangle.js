
class Rectangle {

  /**
   * Constructs a new rectangle given his properties
   * @param x
   * @param y
   * @param width
   * @param height
   */
  constructor(x, y, width, height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }


}



export default Rectangle;